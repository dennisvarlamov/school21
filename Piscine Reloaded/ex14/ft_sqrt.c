int		ft_sqrt(int nb)
{
	int x;
	int y;

	x = 1;
	y = nb;
	if (nb < 0)
		return (0);
	while (nb--)
	{
		if (y == x * x)
			return (x);
		x++;
	}
	return (0);
}
