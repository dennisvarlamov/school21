#include "libft.h"

int					ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*u_s1;
	unsigned char	*u_s2;

	u_s1 = (unsigned char *)s1;
	u_s2 = (unsigned char *)s2;
	while (n-- > 0)
	{
		if (*(u_s1++) != *(u_s2++))
			return ((int)(*(--u_s1) - *(--u_s2)));
	}
	return (0);
}
