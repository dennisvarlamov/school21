#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*str;
	size_t	s1len;

	s1len = s1 ? ft_strlen(s1) : 0;
	if ((str = ft_strnew(s1len)))
		return (ft_strncpy(str, s1, s1len));
	else
		return (NULL);
}
