#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	i;
	int	slen;

	if (s != NULL && f != NULL)
	{
		slen = (int)ft_strlen(s);
		i = 0;
		while (i < slen)
			f(s + i++);
	}
}
