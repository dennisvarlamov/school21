#include "libft.h"

void		ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*lst_next;

	if (lst && f)
	{
		while (lst)
		{
			lst_next = lst->next;
			f(lst);
			lst = lst_next;
		}
	}
}
